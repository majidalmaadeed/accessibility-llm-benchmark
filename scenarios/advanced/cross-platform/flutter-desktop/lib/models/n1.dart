class DataItem {
  final int id;
  final String name;
  final String category;
  final String status;
  final int value;
  final String date;
  final String active;

  DataItem({
    required this.id,
    required this.name,
    required this.category,
    required this.status,
    required this.value,
    required this.date,
    required this.active,
  });
}
